package com.victorcanas.JavaBasicoTema1Ej1;

public class Main {

    public static void main(String[] args){
        int iVar = 1;
        System.out.println("Int: " + iVar);
        byte byVar = 1;
        System.out.println("Byte: " + byVar);
        short sVar = 1;
        System.out.println("Short: " + sVar);
        long lVar = 1L;
        System.out.println("Long: " + lVar);
        float fVar = 1f;
        System.out.println("Float: " + fVar);
        double dVar = 1d;
        System.out.println("Double: " + dVar);
        boolean bVar = false;
        System.out.println("Boolean: " + bVar);
        char cVar = 'h';
        System.out.println("Char: " + cVar);
        String strVar = "hello";
        System.out.println("String: " + strVar);
    }
}
